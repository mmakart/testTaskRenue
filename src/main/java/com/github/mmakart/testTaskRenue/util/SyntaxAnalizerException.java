package com.github.mmakart.testTaskRenue.util;

public class SyntaxAnalizerException extends RuntimeException {

	private static final long serialVersionUID = -1772157544181859141L;

	public SyntaxAnalizerException() {
		super();
	}

	public SyntaxAnalizerException(String message) {
		super(message);
	}

	public SyntaxAnalizerException(Throwable cause) {
		super(cause);
	}

	public SyntaxAnalizerException(String message, Throwable cause) {
		super(message, cause);
	}

	public SyntaxAnalizerException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
